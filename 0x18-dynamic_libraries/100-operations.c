/**
 * add - adds two integers
 * @a: first integer
 * @b: second integer
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtracts two integers
 * @a: first integer
 * @b: second integer
 * Return: the difference
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplies two integers
 * @a: first int
 * @b: second int
 * Return: the product
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divides two numbers
 * @a: first int
 * @b: second int
 * Return: the quotient
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - gets the modulus
 * @a: first int
 * @b: second int
 * Return: the remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
